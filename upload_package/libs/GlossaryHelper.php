    /** ソート済み用語リスト（長い順） */
    private static ?array $sortedTerms = null;

    /** @var string 正規表現パターン（キャッシュ） */
    private static ?string $pattern = null;

    /** 1文字の分類階級用語（前方に漢字・カタカナがあるときはスキップ） */
    private const SINGLE_CHAR_RANKS = ['界', '門', '綱', '目', '科', '属', '種'];

    /**
     * テキスト内の専門用語にツールチップ用 <span> を付与して返す。
     * 入力は htmlspecialchars() 済みであること。
     */
    public static function annotate(string $escapedText): string
    {
        if ($escapedText === '') {
            return '';
        }

        self::buildPattern();

        // 用語を <span> でラップ（同じ用語は最初の出現のみ）
        $annotated = [];
        $result = preg_replace_callback(self::$pattern, function ($m) use (&$annotated) {
            $term = $m[0];
            // 同じ用語は1回だけ注釈
            if (isset($annotated[$term])) {
                return $term;
            }
            $annotated[$term] = true;
            $explanation = htmlspecialchars(self::$glossary[$term], ENT_QUOTES, 'UTF-8');
            return '<span class="glossary-term" tabindex="0" data-glossary="' . $explanation . '">'
                . $term
                . '<svg class="glossary-icon" viewBox="0 0 16 16" fill="none"><circle cx="8" cy="8" r="7" stroke="currentColor" stroke-width="1.5"/><text x="8" y="12" text-anchor="middle" fill="currentColor" font-size="10" font-weight="600">?</text></svg>'
                . '</span>';
        }, $escapedText);

        return $result ?? $escapedText;
    }

    /**
     * 複数テキスト（配列の各要素）に annotate を適用。
     */
    public static function annotateList(array $items): array
    {
        return array_map(function ($item) {
            return self::annotate(htmlspecialchars((string)$item, ENT_QUOTES, 'UTF-8'));
        }, $items);
    }

    /**
     * 正規表現パターンを構築（長い用語から先にマッチ）
     * 1文字の分類階級（科・属・種など）は直前に漢字・カタカナ・ひらがながない場合のみマッチ
     */
    private static function buildPattern(): void
    {
        if (self::$pattern !== null) {
            return;
        }

        $terms = array_keys(self::$glossary);
        // 長い用語から先にマッチさせる
        usort($terms, function ($a, $b) {
            return mb_strlen($b) - mb_strlen($a);
        });
        self::$sortedTerms = $terms;

        $parts = [];
        // 直前がカタカナ or 漢字なら生物名の一部（例: シジュウカラ科）なのでスキップ
        $lookbehind = '(?<![\p{Script=Han}\p{Script=Katakana}])';
        foreach ($terms as $t) {
            $q = preg_quote($t, '/');
            if (in_array($t, self::SINGLE_CHAR_RANKS, true)) {
                // 1文字分類階級: 直前が日本語文字なら「〇〇科」の一部なのでスキップ
                $parts[] = $lookbehind . $q;
            } else {
                $parts[] = $q;
            }
        }

        self::$pattern = '/' . implode('|', $parts) . '/u';
    }

